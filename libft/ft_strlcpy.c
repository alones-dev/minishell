#include "libft.h"

size_t	ft_strlcpy(char	*dest, const char *src, size_t size)
{
	size_t	i;
	size_t	t_src;

	t_src = 0;
	while (src[t_src])
		t_src++;
	if (!size)
		return (t_src);
	i = 0;
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (t_src);
}
